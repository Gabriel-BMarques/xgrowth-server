/**
 * @api {post} v1/auth/refresh-token Refresh Token
 * @apiDescription Refresh expired accessToken
 * @apiVersion 1.0.0
 * @apiName RefreshToken
 * @apiGroup Auth
 * @apiPermission public
 *
 * @apiParam  {String}  email         User's email
 * @apiParam  {String}  refreshToken  Refresh token aquired when user logged in
 *
 * @apiSuccess {String}  tokenType     Access Token's type
 * @apiSuccess {String}  accessToken   Authorization Token
 * @apiSuccess {String}  refreshToken  Token to get a new accessToken after expiration time
 * @apiSuccess {Number}  expiresIn     Access Token's expiration time in miliseconds
 *
 * @apiError (Bad Request 400)  ValidationError  Some parameters may contain invalid values
 * @apiError (Unauthorized 401)  Unauthorized     Incorrect email or refreshToken
 */
router.route('/refresh')
  .post(validate(refresh), controller.refresh);

/**
 * @api {post} v1/auth/reset-password/init Init reset password process
 * @apiDescription Reset password process
 * @apiVersion 1.0.0
 * @apiName ResetPasswordInit
 * @apiGroup Auth
 * @apiPermission public
 *
 * @apiParam    {String}  email   User's email
 *
 * @apiSuccess  (No Content 204)  Successfully reset initiated
 *
 * @apiError    (Bad Request 400) ValidationError  Some parameters may contain invalid values
 */
router.route('/reset-password/init')
  .post(validate(resetInit), controller.resetPasswordInit);

/**
 * @api {post} v1/auth/forgot-password Init forgot password process
 * @apiDescription Forgot password process
 * @apiVersion 1.0.0
 * @apiName ForgotPasswordInit
 * @apiGroup Auth
 * @apiPermission public
 *
 * @apiParam    {String}  email   User's email
 *
 * @apiSuccess  (No Content 204)  Successfully reset initiated
 *
 * @apiError    (Bad Request 400) ValidationError  Some parameters may contain invalid values
 */
router.route('/forgot-password')
  .post(validate(resetInit), controller.forgotPassword);

  /**
 * @api {post} v1/auth/get-reactkey get reactivation link
 * @apiDescription get reactivation link
 * @apiVersion 1.0.0
 * @apiName getReactLink
 * @apiGroup Auth
 * @apiPermission public
 *
 * @apiParam    {String}  email   User's email
 *
 * @apiSuccess  (No Content 204)  Successfully getReactLink
 *
 * @apiError    (Bad Request 400) ValidationError  Some parameters may contain invalid values
 */
router.route('/get-reactlink')
  .post(validate(resetInit), controller.getReactLink);

/**
 * @api {post} v1/auth/facebook Facebook Login
 * @apiDescription Login with facebook. Creates a new user if it does not exist
 * @apiVersion 1.0.0
 * @apiName FacebookLogin
 * @apiGroup Auth
 * @apiPermission public
 *
 * @apiParam  {String}  access_token  Facebook's access_token
 *
 * @apiSuccess {String}  tokenType     Access Token's type
 * @apiSuccess {String}  accessToken   Authorization Token
 * @apiSuccess {String}  refreshToken  Token to get a new accessToken after expiration time
 * @apiSuccess {Number}  expiresIn     Access Token's expiration time in miliseconds
 *
 * @apiError (Bad Request 400)  ValidationError  Some parameters may contain invalid values
 * @apiError (Unauthorized 401)  Unauthorized    Incorrect access_token
 */
router.route('/facebook')
  .post(validate(oAuth), oAuthLogin('facebook'), controller.oAuth);

  /**
 * @api {post} v1/auth/verifyDomain 
 * @apiDescription Verify if user registration domain exists
 * @apiVersion 1.0.0
 * @apiName FacebookLogin
 * @apiGroup Auth
 * @apiPermission public
 *
 * @apiParam  {String}  email  User email
 *
 * @apiSuccess {String}  tokenType     Access Token's type
 * @apiSuccess {String}  accessToken   Authorization Token
 * @apiSuccess {String}  refreshToken  Token to get a new accessToken after expiration time
 * @apiSuccess {Number}  expiresIn     Access Token's expiration time in miliseconds
 *
 * @apiError (Bad Request 400)  ValidationError  Some parameters may contain invalid values

 */
router.route('/verifyDomain')
  .post(controller.verifyDomain);

module.exports = router;
